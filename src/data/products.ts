/** 사이즈 선택이 필요한지 */
export function productSizeKind(
  p: Product
): "clothing" | "shoe" | null {
  if (p.shoeSizes?.length) return "shoe";
  if (p.clothingSizes?.length) return "clothing";
  return null;
}

export function sizeOptionsFor(p: Product): string[] {
  return p.shoeSizes ?? p.clothingSizes ?? [];
}

/** 9층 식당별로 상품 묶기 */
export function groupFoodByRestaurant(items: Product[]): {
  restaurant: string;
  items: Product[];
}[] {
  const foods = items.filter(
    (p) => p.category === "식품" && p.restaurant
  );
  const map = new Map<string, Product[]>();
  for (const p of foods) {
    const r = p.restaurant!;
    const arr = map.get(r) ?? [];
    arr.push(p);
    map.set(r, arr);
  }
  return [...map.entries()].map(([restaurant, arr]) => ({
    restaurant,
    items: arr,
  }));
}

